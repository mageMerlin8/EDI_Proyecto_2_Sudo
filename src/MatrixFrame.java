    class GridButtonAction extends AbstractAction{
        int col, row;
        public GridButtonAction(int row, int col){
            super();
            this.col = col;
            this.row = row;
        }
        @Override
        public void actionPerformed(ActionEvent e) {
            //System.out.println("("+row+", "+col+")");
            celdaAct = new Celda(row, col);
            numerosPosibles = sudo.numerosPosibles(celdaAct);
            jLabel1.setText("Celda: " + "(" + row + ", " + col + ")");
            enableButtons();
            setGrid();
        }
    }
    
    class  NumberButtonAction extends AbstractAction{
        int num;
        boolean valid;
        
        public NumberButtonAction(int num, boolean valid){
            super();
            this.num = num;
            this.valid = valid;
        }
        @Override
        public void actionPerformed(ActionEvent e) {
            if(valid){
                jLabel2.setVisible(false);
                if(num!=0)
                    sudo.insertaNumero(num, celdaAct);
                else
                    sudo.borraCelda(celdaAct);
                setGrid();
                printGrid();
            }else{
                jLabel2.setVisible(true);
                jLabel2.setText("El numero " + num + " no es valido.");
                setGrid(num);
            }
        }
        
    }
    
    class SolveAction extends AbstractAction{
        @Override
        public void actionPerformed(ActionEvent e) {
            if(!sudo.solveSudo()){
                jLabel2.setText("No se encontro solucion");
                jLabel2.setVisible(true);
            }
            setGrid();
        }
    }
    class ClearAction extends AbstractAction{
        @Override
        public void actionPerformed(ActionEvent e) {
            sudo = new Sudoku();
            setGrid();
        }
    }
    
