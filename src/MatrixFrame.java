    private static Sudoku sudoFromFile(File arch){
        Scanner scan;
        try {
            scan = new Scanner(arch);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MatrixFrame.class.getName()).log(Level.SEVERE, null, ex);
            scan = null;
        }
        Sudoku newSudo = new Sudoku();
        for(int i = 0; i < 9; i++)
            for(int j = 0; j < 9; j++){
                newSudo.insertaNumero(scan.nextInt() , new Celda(i,j));
            }
        return newSudo;
    }
    
