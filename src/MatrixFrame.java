    private void asignaAcciones(){
        //Numeros
        enableButtons();
        //grid
        for(int i = 0; i < 9; i++)
            for(int j = 0; j < 9; j++)
                botonesGrid[i][j].setAction(new GridButtonAction(i,j));
    }
    private void enableButtons(){
        botones[0].setAction(new NumberButtonAction(0, true));
            botones[0].setText(""+0);
        for(int i = 1; i < 10; i++){
            botones[i].setAction(new NumberButtonAction(i, numerosPosibles.contains(i)));
            botones[i].setText(""+(i));
        }
    }
    //Lee todos los valores de la matriz de valores
    //pinta de rojo los que son iguales a la seleccion actual
    private void setGrid(){
        int curr = sudo.getVal(celdaAct);
        for(int i = 0; i < 9; i++)
            for(int j = 0; j < 9; j++){
                if(curr == sudo.getVal(j,i))
                    botonesGrid[i][j].setBackground(OTHER_BUTTON_COLOR);
                else
                    botonesGrid[i][j].setBackground(DEFAULT_BUTTON_COLOR);
                botonesGrid[i][j].setText(""+sudo.getVal(j,i));
            }
        botonesGrid[celdaAct.getRow()][celdaAct.getCol()].setBackground(SELECTED_BUTTON_COLOR);
    }
    //Hace lo mismo que la pasada pero pinta de rojo algun numero
    //se usa para cuando el usuario selecciona un valor que no cabe
    //pinta de rojo los que son iguales para que sea evidente porque no se puede
    private void setGrid(int curr){
        for(int i = 0; i < 9; i++)
            for(int j = 0; j < 9; j++){
                if(curr == sudo.getVal(j,i))
                    botonesGrid[i][j].setBackground(OTHER_BUTTON_COLOR);
                else
                    botonesGrid[i][j].setBackground(DEFAULT_BUTTON_COLOR);
                botonesGrid[i][j].setText(""+sudo.getVal(j,i));
                    
            }
        botonesGrid[celdaAct.getRow()][celdaAct.getCol()].setBackground(SELECTED_BUTTON_COLOR);
    }
    
    
